    private void divideActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_divideActionPerformed
        if (m == true && e == true) {
            number1 = Double.parseDouble(display.getText());
            history.setText(String.valueOf(form.format(number1)));
        } else if (m == true) {
            number2 = Double.parseDouble(display.getText());
            display.setText(String.valueOf(form.format(number1 * number2)));
        }
        if (a == true && e == true) {
            number1 = Double.parseDouble(display.getText());
            history.setText(String.valueOf(form.format(number1)));
        } else if (a == true) {
            number2 = Double.parseDouble(display.getText());
            display.setText(String.valueOf(form.format(number1 + number2)));
        }
        if (s == true && e == true) {
            number1 = Double.parseDouble(display.getText());
            history.setText(String.valueOf(form.format(number1)));
        } else if (s == true) {
            number2 = Double.parseDouble(display.getText());
            display.setText(String.valueOf(form.format(number1 - number2)));
        }
        if (d == true && e == true) {
            number1 = Double.parseDouble(display.getText());
            history.setText(String.valueOf(form.format(number1)));
        } else if (d == true) {
            number2 = Double.parseDouble(display.getText());
            display.setText(String.valueOf(form.format(number1 / number2)));
        }
        
        a = false;
        m = false;
        d = true;
        s = false;
        e = false;
        n = false;
        number1 = Double.parseDouble(display.getText());
        history.setText(history.getText() + "/");
    }//GEN-LAST:event_divideActionPerformed

