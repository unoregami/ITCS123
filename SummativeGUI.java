    public int ternarySearch(int[] array, int key, int l, int r) {
        if (r >= l) {
            int mid1 = l + (r - l) / 3;
            int mid2 = r - (r - l) / 3;
        
            if (array[mid1] == key) {
                return mid1;
            }
            if (array[mid2] == key) {
                return mid2;
            }
        
            if (key < array[mid1]) {
                r = mid1 - 1;
                return ternarySearch(array, key, l, r);
            } else if (key > array[mid2]) {
                l = mid2 + 1;
                return ternarySearch(array, key, l, r);
            } else {
                l = mid1 + 1;
                r = mid2 - 1;
                return ternarySearch(array, key, l, r);
            }
        }
        return -1;
    }
    
    public void reset() {
        sortarray = new int[0];
        searcharray = new int[0];
        key = 0;
        
        tfSortInput.setText(null);
        tfSortArray.setText(null);
        tfSearchInput.setText(null);
        tfSearchKey.setText(null);
        tfSearchKey1.setText(null);
        tfSearchArray.setText(null);
        tfResult.setText(null);
    }
    
