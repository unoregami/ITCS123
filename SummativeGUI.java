    int[] searcharray = new int[0];
    int key;
    
    private void tfSortInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfSortInputActionPerformed
        int n = sortarray.length;
        sortarray = Arrays.copyOf(sortarray, n + 1);
        sortarray[n] = Integer.parseInt(tfSortInput.getText());
        
        tfSortArray.setText(Arrays.toString(sortarray));
        tfSortInput.setText(null);
    }//GEN-LAST:event_tfSortInputActionPerformed

    private void btnSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortActionPerformed
        for (int i = sortarray.length - 1; i >= 0; i--) {
            for (int j = sortarray.length - 1; j > 0; j--) {
                if (sortarray[j] > sortarray[j-1]) {
                    int stock = sortarray[j];
                    sortarray[j] = sortarray[j-1];
                    sortarray[j-1] = stock;
                }
            }
        }
        tfResult.setText(Arrays.toString(sortarray));
    }//GEN-LAST:event_btnSortActionPerformed

