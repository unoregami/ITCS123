    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        int result = ternarySearch(searcharray, key, 0, searcharray.length - 1);
        for (int i = 0; i < searcharray.length - 1; i++) {
            if (searcharray[i] > searcharray[i+1])
                result = -2;
        }
        
        switch (result) {
            case -2:
                tfResult.setText("The Array should be sorted ascendingly.");
                break;
            case -1:
                tfResult.setText("The Key is not in the Array.");
                break;
            default:
                tfResult.setText("The Key '" + key + "' is in Position " + result);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

