    int[] array = new int[0];
    int n = 1;
    
    private void tfInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfInputActionPerformed
        try {
            array = Arrays.copyOf(array, n);
            array[n-1] = Integer.parseInt(tfInput.getText());
            if (n == 1)
                tfArray.setText(tfInput.getText());
            else
                tfArray.setText(tfArray.getText() + ", " + tfInput.getText());
            tfInput.setText(null);
            n++;
        } catch (Exception e) {
            tfInput.setText(null);
            JOptionPane.showMessageDialog(null, "Input a value!", "ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_tfInputActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int n = array.length;
        for (int i = 1; i < n; ++i) {
            int key = array[i];
            int j = i - 1;
            
            while (j >= 0 && array[j] > key) {
                array[j + 1] = array[j];
                j = j - 1;
            }
            array[j + 1] = key;
        }
        
        tfResult.setText(Arrays.toString(array));
    }//GEN-LAST:event_jButton1ActionPerformed

